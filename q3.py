# 3. Arithmetic Operations
# Prompt for two numbers. 
# Compute and display the sum, difference, product, division, and modulus of those two numbers.

num1=float(input("Enter the 1st num: "))
num2=float(input("enter the 2nd num: "))

print(f"Sum: {num1 + num2}")
print(f"Difference: {num2 - num1}")
print(f"Product: {num1 * num2}")
print(f"Division: {num2 / num1}")
print(f"Modulus: {num1 % num2}")
